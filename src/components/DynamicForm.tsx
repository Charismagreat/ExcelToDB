'use client';

import React from 'react';
import { ChevronDown, Loader2, Check, Upload, X, Phone, Mail, FileText, Type } from 'lucide-react';
import { uploadFileAction } from '@/app/actions';

interface Column {
  name: string;
  type: string;
  isAutoGenerated?: boolean;
  isRequired?: boolean;
  options?: string[];
  autoPrefix?: string;
  description?: string;
}

interface DynamicFormProps {
  columns: Column[];
  initialData?: any;
  onSubmit: (data: any) => Promise<void>;
  onCancel?: () => void;
  onStatusShow?: (title: string, message: string, type: 'success' | 'error' | 'info') => void;
}

export default function DynamicForm({ columns, initialData = {}, onSubmit, onCancel, onStatusShow }: DynamicFormProps) {
  const [formData, setFormData] = React.useState(initialData);
  const [isSubmitting, setIsSubmitting] = React.useState(false);

  const handleChange = (e: React.ChangeEvent<HTMLInputElement | HTMLSelectElement>) => {
    const { name, value, type } = e.target as any;
    setFormData((prev: any) => ({
      ...prev,
      [name]: type === 'number' ? (value === '' ? '' : Number(value)) : value,
    }));
  };

  const handleFileChange = async (fieldName: string, file: File) => {
    const formDataObj = new FormData();
    formDataObj.append('file', file);
    try {
        const result = await uploadFileAction(formDataObj);
        setFormData((prev: any) => ({ ...prev, [fieldName]: result.url }));
        if (onStatusShow) onStatusShow('мЧЕл°ЬлУЬ мД±к≥µ', 'нММмЭЉмЭі мЧЕл°ЬлУЬлРШмЧИмКµлЛИлЛ§.', 'success');
    } catch (error) {
        if (onStatusShow) onStatusShow('мЧЕл°ЬлУЬ мЛ§нМ®', 'нММмЭЉ мЧЕл°ЬлУЬ м§С мШ§л•Шк∞А л∞ЬмГЭнЦИмКµлЛИлЛ§.', 'error');
    }
  };

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    
    // мµЬмҐЕ мЬ†нЪ®мД± к≤АмВђ
    for (const col of columns) {
        if (col.isAutoGenerated) continue;
        const val = formData[col.name];

        if (col.isRequired && (val === undefined || val === null || val === '')) {
            if (onStatusShow) onStatusShow('мЮЕл†• лИДлЭљ', `'${col.name}' нХДлУЬлКФ нХДмИШ мЮЕл†• нХ≠л™©мЮЕлЛИлЛ§.`, 'error');
            else alert(`'${col.name}' нХДлУЬлКФ нХДмИШ мЮЕл†• нХ≠л™©мЮЕлЛИлЛ§.`);
            return;
        }

        const isNumericType = col.type === 'number' || col.type === 'currency';
        if (isNumericType && val !== '' && val !== undefined && isNaN(Number(val?.toString().replace(/[,вВ©$¬•вВђ]/g, '')))) {
            if (onStatusShow) onStatusShow('нШХмЛЭ мШ§л•Ш', `'${col.name}' нХДлУЬмЧРлКФ мИЂмЮРлІМ мЮЕл†• к∞АлК•нХ©лЛИлЛ§.`, 'error');
            else alert(`'${col.name}' нХДлУЬмЧРлКФ мИЂмЮРлІМ мЮЕл†• к∞АлК•нХ©лЛИлЛ§.`);
            return;
        }

        if (col.type === 'email' && val && !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(val)) {
            if (onStatusShow) onStatusShow('нШХмЛЭ мШ§л•Ш', 'мШђл∞Фл•Є мЭіл©ФмЭЉ нШХмЛЭмЭі мХДлЛЩлЛИлЛ§.', 'error');
            return;
        }

        if (col.type === 'phone' && val && !/^[\d\+\-\s]+$/.test(val)) {
            if (onStatusShow) onStatusShow('нШХмЛЭ мШ§л•Ш', 'мШђл∞Фл•Є м†ДнЩФл≤ИнШЄ нШХмЛЭмЭі мХДлЛЩлЛИлЛ§.', 'error');
            return;
        }
    }

    setIsSubmitting(true);
    try {
        await onSubmit(formData);
        // м†АмЮ• мД±к≥µ мЛЬмЧРлІМ мЮЕл†• нХДлУЬ міИкЄ∞нЩФ
        setFormData(initialData);
    } catch (error) {
        // мЧРлЯђлКФ лґАл™®(ReportDataEditor)мЧРмДЬ л™®лЛђл°Ь л≥імЧђм£ЉлѓАл°Ь мЧђкЄ∞мДЬлКФ нПЉ лН∞мЭінД∞лІМ мЬ†мІА
        console.error('м†ЬмґЬ м§С мШ§л•Ш л∞ЬмГЭ:', error);
    } finally {
        setIsSubmitting(false);
    }
  };

  return (
    <form onSubmit={handleSubmit} className="space-y-6 bg-white p-8 border rounded-xl shadow-sm">
      <div className="grid grid-cols-1 md:grid-cols-2 gap-6">
        {columns.map((col) => {
          const val = formData[col.name];
          const isNumericType = col.type === 'number' || col.type === 'currency';
          const isInvalidType = isNumericType && val !== '' && val !== undefined && isNaN(Number(val?.toString().replace(/[,вВ©$¬•вВђ]/g, '')));
          
          return (
            <div key={col.name} className={`flex flex-col gap-2 ${col.isAutoGenerated ? 'opacity-70' : ''}`}>
              <label htmlFor={col.name} className="text-sm font-semibold text-gray-700 flex items-center justify-between">
                <span className="flex items-center gap-1.5 min-w-0">
                  <span className="truncate">{col.name}</span>
                  {col.isRequired && !col.isAutoGenerated && <span className="text-red-500">*</span>}
                  {!col.isAutoGenerated && (
                      <span className={`
                        text-[8px] font-black px-1.5 py-0.5 rounded border uppercase tracking-tighter shrink-0
                        ${col.type === 'number' || col.type === 'currency' ? 'bg-blue-50 text-blue-600 border-blue-100' : 
                          col.type === 'date' ? 'bg-green-50 text-green-600 border-green-100' : 
                          col.type === 'select' || col.type === 'boolean' ? 'bg-purple-50 text-purple-600 border-purple-100' :
                          col.type === 'file' ? 'bg-orange-50 text-orange-600 border-orange-100' :
                          'bg-gray-50 text-gray-500 border-gray-200'}
                      `}>
                        {col.type}
                      </span>
                  )}
                </span>
                {col.isAutoGenerated && <span className="text-[10px] font-black text-indigo-500 bg-indigo-50 px-2 py-0.5 rounded-md">AUTO_KEY</span>}
              </label>

              <div className="relative flex flex-col gap-2">
                <div className="flex items-stretch relative">
                  {col.type === 'currency' && (
                      <select 
                        className="bg-gray-50 border border-r-0 border-gray-200 rounded-l-lg px-2 text-xs font-bold text-gray-500 outline-none focus:ring-0 appearance-none cursor-pointer hover:bg-gray-100 transition-colors"
                        onChange={(e) => {
                            const symbol = e.target.value;
                            const currentVal = formData[col.name]?.toString() || '';
                            const cleanVal = currentVal.replace(/^[вВ©$¬•вВђ]\s?/, '');
                            setFormData((prev: any) => ({ ...prev, [col.name]: `${symbol} ${cleanVal}`.trim() }));
                        }}
                        defaultValue="вВ©"
                      >
                          <option value="вВ©">вВ©</option>
                          <option value="$">$</option>
                          <option value="вВђ">вВђ</option>
                          <option value="¬•">¬•</option>
                      </select>
                  )}

                  {(col.type === 'email' || col.type === 'phone') && (
                      <div className="absolute left-3 top-1/2 -translate-y-1/2 text-gray-400 z-10">
                          {col.type === 'email' ? <Mail size={14} /> : <Phone size={14} />}
                      </div>
                  )}

                  {col.type === 'select' ? (
                      <div className="relative w-full">
                          <select
                            id={col.name}
                            name={col.name}
                            value={formData[col.name] || ''}
                            onChange={handleChange}
                            className="w-full px-4 py-2 border border-gray-200 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 outline-none transition-all font-medium appearance-none bg-white cursor-pointer hover:bg-gray-50"
                          >
                              <option value="">мД†нГЭнХШмДЄмЪФ</option>
                              {col.options?.map(opt => <option key={opt} value={opt}>{opt}</option>)}
                          </select>
                          <ChevronDown size={14} className="absolute right-3 top-1/2 -translate-y-1/2 text-gray-400 pointer-events-none" />
                      </div>
                  ) : col.type === 'boolean' ? (
                      <div className="flex items-center gap-2 bg-gray-50 p-1 rounded-xl border border-gray-100 w-full">
                          <button
                            type="button"
                            onClick={() => setFormData((prev: any) => ({ ...prev, [col.name]: 'Yes' }))}
                            className={`flex-1 flex items-center justify-center gap-2 py-1.5 rounded-lg text-xs font-black transition-all ${val === 'Yes' ? 'bg-white text-blue-600 shadow-sm' : 'text-gray-400 hover:text-gray-600'}`}
                          >
                              <Check size={14} /> мШИ (Yes)
                          </button>
                          <button
                            type="button"
                            onClick={() => setFormData((prev: any) => ({ ...prev, [col.name]: 'No' }))}
                            className={`flex-1 flex items-center justify-center gap-2 py-1.5 rounded-lg text-xs font-black transition-all ${val === 'No' ? 'bg-white text-red-600 shadow-sm' : 'text-gray-400 hover:text-gray-600'}`}
                          >
                              <X size={14} /> мХДлЛИмШ§ (No)
                          </button>
                      </div>
                  ) : col.type === 'textarea' ? (
                      <textarea
                        id={col.name}
                        name={col.name}
                        value={formData[col.name] || ''}
                        onChange={(e: any) => handleChange(e)}
                        className="w-full px-4 py-2 border border-gray-200 rounded-lg focus:ring-2 focus:ring-blue-500 focus:border-blue-500 outline-none transition-all font-medium min-h-[100px] resize-none"
                        placeholder={`${col.name} лВімЪ©мЭД мЮЕл†•нХШмДЄмЪФ...`}
                      />
                  ) : col.type === 'file' ? (
                      <div className="w-full flex flex-col gap-2">
                          <label className="flex items-center justify-center gap-2 w-full py-8 border-2 border-dashed border-gray-200 rounded-xl cursor-pointer hover:bg-gray-50 hover:border-blue-400 transition-all text-gray-400 hover:text-blue-500 group">
                              <input 
                                type="file" 
                                className="hidden" 
                                onChange={(e) => e.target.files?.[0] && handleFileChange(col.name, e.target.files[0])} 
                              />
                              <Upload size={20} className="group-hover:scale-110 transition-transform" />
                              <span className="text-xs font-bold">{val ? 'нММмЭЉ л≥Ак≤љнХШкЄ∞' : 'нММмЭЉ мЧЕл°ЬлУЬ (мЭілѓЄмІА/лђЄмДЬ)'}</span>
                          </label>
                          {val && (
                              <div className="flex items-center gap-3 p-3 bg-blue-50 rounded-xl border border-blue-100 animate-in fade-in zoom-in duration-300">
                                  {val.match(/\.(jpg|jpeg|png|gif|webp)$/i) ? (
                                      <img src={val} alt="Preview" className="w-12 h-12 rounded-lg object-cover shadow-sm bg-white" />
                                  ) : (
                                      <div className="w-12 h-12 rounded-lg bg-white flex items-center justify-center shadow-sm"><FileText size={20} className="text-blue-500" /></div>
                                  )}
                                  <div className="flex-1 min-w-0">
                                      <p className="text-[10px] font-black text-blue-600 uppercase">Attached File</p>
                                      <p className="text-xs font-medium text-gray-700 truncate">{val.split('/').pop()}</p>
                                  </div>
                                  <button type="button" onClick={() => setFormData((prev: any) => ({ ...prev, [col.name]: '' }))} className="text-gray-400 hover:text-red-500"><X size={16} /></button>
                              </div>
                          )}
                      </div>
                  ) : (
                      <input
                        id={col.name}
                        name={col.name}
                        type={col.type === 'date' ? 'date' : 'text'}
                        value={
                            col.isAutoGenerated 
                            ? (formData[col.name] || (col.autoPrefix ? `${col.autoPrefix}000000` : 'мЮРлПЩ мГЭмД±')) 
                            : (col.type === 'currency' 
                               ? (formData[col.name]?.toString().replace(/^[вВ©$¬•вВђ]\s?/, '') || '') 
                               : (formData[col.name] || ''))
                        }
                        onChange={(e) => {
                            if (col.type === 'currency') {
                                const val = e.target.value;
                                const existingSymbol = formData[col.name]?.toString().match(/^[вВ©$¬•вВђ]/)?.[0] || 'вВ©';
                                setFormData((prev: any) => ({ ...prev, [col.name]: `${existingSymbol} ${val}`.trim() }));
                            } else {
                                handleChange(e);
                            }
                        }}
                        readOnly={col.isAutoGenerated}
                        className={`
                           w-full px-4 py-2 border rounded-lg transition-all
                           ${col.type === 'currency' ? 'rounded-l-none' : ''}
                           ${(col.type === 'email' || col.type === 'phone') ? 'pl-10' : ''}
                           ${col.isAutoGenerated ? 'bg-gray-50 text-gray-400 border-gray-100 cursor-not-allowed' : 
                             isInvalidType ? 'border-red-500 bg-red-50 focus:ring-red-500' : 'focus:ring-2 focus:ring-blue-500 focus:border-blue-500 border-gray-200'}
                        `}
                        placeholder={col.isAutoGenerated ? 'мЮРлПЩмЬЉл°Ь лґАмЧђлРШлКФ к≥†мЬ†нВ§мЮЕлЛИлЛ§' : `${col.name} мЮЕл†•`}
                        required={col.isRequired && !col.isAutoGenerated}
                      />
                  )}
                </div>
                {isInvalidType && (
                    <p className="text-[10px] font-bold text-red-500 flex items-center gap-1 animate-in fade-in slide-in-from-top-1">
                        <span className="bg-red-500 text-white rounded-full w-3 h-3 flex items-center justify-center text-[8px]">!</span>
                        мИЂмЮР лН∞мЭінД∞лІМ мЮЕл†• к∞АлК•нХ©лЛИлЛ§.
                    </p>
                )}
              </div>
            </div>
          );
        })}
      </div>
      <div className="flex justify-end gap-3 pt-4 border-t">
        {onCancel && (
          <button
            type="button"
            onClick={onCancel}
            className="px-6 py-2 text-sm font-medium text-gray-600 bg-gray-100 rounded-lg hover:bg-gray-200 transition-colors"
          >
            мЈ®мЖМ
          </button>
        )}
        <button
          type="submit"
          disabled={isSubmitting}
          className="px-6 py-2 text-sm font-medium text-white bg-blue-600 rounded-lg hover:bg-blue-700 shadow-md transition-colors disabled:opacity-50 flex items-center gap-2"
        >
          {isSubmitting ? (
              <>
                <Loader2 size={16} className="animate-spin" />
                м≤Шл¶ђ м§С...
              </>
          ) : 'м†АмЮ•нХШкЄ∞'}
        </button>
      </div>
    </form>
  );
}
