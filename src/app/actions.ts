'use server';

import { revalidatePath } from 'next/cache';
import prisma from '@/lib/prisma';
import { parseExcelWorkbook } from '@/lib/excel-parser';
import { analyzeExcelImage } from '@/lib/ai-vision';

export async function uploadExcelAction(formData: FormData, userId: string) {
  const file = formData.get('file') as File;
  const configsJson = formData.get('configsJson') as string;
  if (!file) throw new Error('파일이 없습니다.');

  const buffer = Buffer.from(await file.arrayBuffer());
  const configs = configsJson ? JSON.parse(configsJson) : [];
  
  // Use the advanced parser with user-defined configs (header row index, selected fields)
  const sheetResults = parseExcelWorkbook(buffer, configs);

  // Store each sheet/table as a separate report
  for (const sheet of sheetResults) {
    for (const table of sheet.tables) {
      if (table.columns.length === 0) continue; // Skip if no columns selected

      await prisma.report.create({
        data: {
          name: table.name,
          sheetName: sheet.sheetName,
          columns: JSON.stringify(table.columns),
          ownerId: userId,
          rows: {
            create: table.rows.map(row => ({
              data: JSON.stringify(row)
            }))
          }
        }
      });
    }
  }

  revalidatePath('/');
}

export async function addRowAction(reportId: string, rowData: any) {
  const report = await prisma.report.findUnique({ where: { id: reportId } });
  if (!report) throw new Error('보고서를 찾을 수 없습니다.');

  const columns = JSON.parse(report.columns);
  const autoGenCols = columns.filter((c: any) => c.isAutoGenerated);
  
  const finalData = { ...rowData };
  
  // 1. DID-일련번호 자동 키 생성
  const rowCount = await prisma.reportRow.count({ where: { reportId } });
  const nextSerial = (rowCount + 1).toString().padStart(6, '0');
  
  autoGenCols.forEach((col: any) => {
    finalData[col.name] = `DID-${nextSerial}`;
  });

  // Server-side Validation
  for (const col of columns) {
    if (col.isAutoGenerated) continue;
    const val = finalData[col.name];

    if (col.isRequired && (val === undefined || val === null || val === '')) {
        throw new Error(`'${col.name}' 필드는 필수입니다.`);
    }

    if (col.type === 'number' && val !== '' && val !== null && val !== undefined) {
        if (isNaN(Number(val))) {
            throw new Error(`'${col.name}' 필드에는 숫자만 입력 가능합니다.`);
        }
    }
  }

  await prisma.reportRow.create({
    data: {
      reportId,
      data: JSON.stringify(finalData)
    }
  });
  revalidatePath(`/report/${reportId}`);
}

export async function deleteReportAction(reportId: string) {
    await prisma.reportRow.deleteMany({ where: { reportId } });
    await prisma.report.delete({ where: { id: reportId } });
    revalidatePath('/');
}

export async function deleteRowsAction(reportId: string, rowIds: string[]) {
    await prisma.reportRow.deleteMany({
        where: { id: { in: rowIds } }
    });
    revalidatePath(`/report/${reportId}`);
}

export async function renameReportAction(reportId: string, newName: string) {
    await prisma.report.update({
        where: { id: reportId },
        data: { name: newName }
    });
    revalidatePath('/');
    revalidatePath(`/report/${reportId}`);
}

export async function updateReportSchemaAction(reportId: string, columns: any[]) {
    await prisma.report.update({
        where: { id: reportId },
        data: { columns: JSON.stringify(columns) }
    });
    revalidatePath(`/report/${reportId}`);
}

export async function analyzeExcelScreenshotAction(formData: FormData) {
  const image = formData.get('image') as File;
  if (!image) throw new Error('이미지 파일이 없습니다.');

  const arrayBuffer = await image.arrayBuffer();
  const buffer = Buffer.from(arrayBuffer);
  const base64 = buffer.toString('base64');
  const mimeType = image.type;

  return await analyzeExcelImage(base64, mimeType);
}

export async function addRowsAction(reportId: string, rows: any[]) {
    const report = await prisma.report.findUnique({ where: { id: reportId } });
    if (!report) throw new Error('보고서를 찾을 수 없습니다.');
  
    const columns = JSON.parse(report.columns);
  
    const initialRowCount = await prisma.reportRow.count({ where: { reportId } });

    // 각 행당 유효성 검사 및 정제
    const cleanedRows = rows.map((rowData, index) => {
      // rowData is { "Col Name": value, ... }
      const finalData: any = {};
      const nextSerial = (initialRowCount + index + 1).toString().padStart(6, '0');
      
      // 1. DID-일련번호 자동 키 생성
      columns.forEach((col: any) => {
        if (col.isAutoGenerated) {
            finalData[col.name] = `DID-${nextSerial}`;
        } else {
            // Excel 에서 읽어온 데이터 매핑 (대소문자/공백 무시 매칭 가능하지만 일단 정확히 매칭)
            finalData[col.name] = rowData[col.name];
        }
      });
  
      // 2. 유효성 검사
      for (const col of columns) {
        if (col.isAutoGenerated) continue;
        const val = finalData[col.name];
  
        if (col.isRequired && (val === undefined || val === null || val === '')) {
            // 필수 체크 (단, 0은 허용)
            if (val !== 0) throw new Error(`필드 '${col.name}'에 값이 누락되었습니다.`);
        }
  
        if ((col.type === 'number' || col.type === 'currency') && val !== '' && val !== null && val !== undefined) {
            const cleanVal = val.toString().replace(/[,₩$¥€]/g, '').trim();
            if (isNaN(Number(cleanVal))) {
                throw new Error(`필드 '${col.name}'에는 숫자만 입력 가능합니다. (입력값: ${val})`);
            }
            finalData[col.name] = Number(cleanVal);
        }

        // 3. 목록 선택(Select) 유효성 검사
        if (col.type === 'select' && col.options && val !== '' && val !== null && val !== undefined) {
            if (!col.options.includes(val.toString().trim())) {
                throw new Error(`[${col.name}] 필드에 잘못된 값 '${val}'이(가) 입력되었습니다. 허용되는 값: ${col.options.join(', ')}`);
            }
        }
      }
      return finalData;
    });
  
    // 트랜잭션으로 일괄 등록
    await prisma.$transaction(
      cleanedRows.map(data => 
        prisma.reportRow.create({
          data: {
            reportId,
            data: JSON.stringify(data)
          }
        })
      )
    );
  
    revalidatePath(`/report/${reportId}`);
    return { success: true };
}
