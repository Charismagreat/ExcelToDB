// auto_repair.js
const fetch = require('node-fetch');

async function run() {
    const API_URL = 'http://localhost:8080';
    const API_KEY = process.env.NEXT_PUBLIC_EGDESK_API_KEY || 'a87d1623-34c1-43fe-87c8-9fa8263d71a4';

    // 1. Get all reports
    const reqAll = await fetch(`${API_URL}/user-data/tools/call`, {
        method: 'POST',
        headers: {'Content-Type': 'application/json', 'X-Api-Key': API_KEY},
        body: JSON.stringify({tool: 'user_data_query', arguments: {tableName: 'report', limit: 1000}})
    });
    const resAll = await reqAll.json();
    const reportsAll = JSON.parse(resAll.result.content[0].text).rows || [];
    const reportsActive = reportsAll.filter(r => String(r.isDeleted) === '0');

    // 2. Loop and sync
    for (const report of reportsActive) {
        if (!report.tableName) continue;
        
        // Physical count
        const preq = await fetch(`${API_URL}/user-data/tools/call`, {
            method: 'POST',
            headers: {'Content-Type': 'application/json', 'X-Api-Key': API_KEY},
            body: JSON.stringify({tool: 'user_data_query', arguments: {tableName: report.tableName, limit: 10000}})
        });
        const pRows = JSON.parse((await preq.json()).result.content[0].text).rows || [];
        
        // Virtual rows
        const vreq = await fetch(`${API_URL}/user-data/tools/call`, {
            method: 'POST',
            headers: {'Content-Type': 'application/json', 'X-Api-Key': API_KEY},
            body: JSON.stringify({tool: 'user_data_query', arguments: {tableName: 'report_row', limit: 10000}})
        });
        const allVRows = JSON.parse((await vreq.json()).result.content[0].text).rows || [];
        const vRows = allVRows.filter(r => r.reportId === String(report.id));
        
        const virtualCount = vRows.length;
        const physicalCount = pRows.length;
        
        if (virtualCount < physicalCount) {
            console.log(`Mismatch on ${report.name}: Virtual ${virtualCount}, Physical ${physicalCount}. Repairing...`);
            
            // Insert missing into report_row
            // Only those not present
            const toInsert = [];
            
            // get schema
            const cols = JSON.parse(report.columns || "[]");
            const idCol = cols.find(c => c.isAutoGenerated)?.name || null;
            
            if (virtualCount === 0 || idCol === null) {
                // If it's 0, just push all
                for (const pRow of pRows) {
                    toInsert.push(pRow);
                }
            } else {
                console.log("Partial mismatch not handled yet.");
            }
            
            if (toInsert.length > 0) {
                const inserts = toInsert.map(pData => ({
                    id: Math.random().toString(36).substring(2, 10),
                    reportId: String(report.id),
                    data: JSON.stringify(pData),
                    isDeleted: '0',
                    createdAt: new Date().toISOString(),
                    updatedAt: new Date().toISOString()
                }));
                
                const ireq = await fetch(`${API_URL}/user-data/tools/call`, {
                    method: 'POST',
                    headers: {'Content-Type': 'application/json', 'X-Api-Key': API_KEY},
                    body: JSON.stringify({
                        tool: 'user_data_insert_rows', 
                        arguments: {
                            tableName: 'report_row', 
                            rows: inserts
                        }
                    })
                });
                const res = await ireq.json();
                console.log("Repaired!", res);
            }
        }
    }
}
run();
